num1 = 50
num2 = 7


# Suma
result = num1 + num2
print(f"Suma: {result}")

# Resta
result = num1 - num2
print(f"Resta: {result}")

# Multiplicación
result = num1 * num2
print(f"Multiplicacion: {result}")

# División
result = num1 / num2
print(f"División: {result}")

# Resto
result = num1 % num2
print(f"Resto: {result}")

# Potencia
result = num1 ** num2
print(f"Potencia: {result}")

# División int
result = num1 // num2
print(f"División result int: {result}")
